#include "ft_printf.h"

int		len_digit(long int n)
{
	int i;
	int check;

	i = 0;
	check = 0;
	if (n < 0)
	{
		i++;
		check++;
		n = n * -1;
	}
	if (n > -10 && n < 10)
	{
		return (i + 1);
	}
	while (n != 0)
	{
		n = n / 10;
		i++;
	}
	if (check == 1)
		n = n * -1;
	return (i);
}
